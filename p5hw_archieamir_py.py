import random
import time


class Ability:
    def __init__(self, name, type_name, energy_cost, base_damage, effect=None):
        self.name = name
        self.type_name = type_name
        self.energy_cost = energy_cost
        self.base_damage = base_damage
        self.effect = effect

    def execute(self, user, target):
        if user.energy < self.energy_cost:
            print(f"{user.name} doesn't have enough energy to use {self.name}.\n")
            return False

        user.energy -= self.energy_cost
        damage = max(self.base_damage + user.strength - target.defense, 0)
        target.take_damage(damage)
        print(f"ðŸ’¥ {user.name} uses a {self.type_name} attack: {self.name}! Deals {damage} damage to {target.name}!")

        if self.effect:
            self.effect(user, target)
        return True

class Fighter:
    def __init__(self, name, style, health, energy, strength, defense, speed, ability):
        self.name = name
        self.style = style
        self.health = health
        self.energy = energy
        self.strength = strength
        self.defense = defense
        self.speed = speed
        self.ability = ability
        self.stunned = False

    def is_alive(self):
        return self.health > 0

    def take_damage(self, dmg):
        self.health -= dmg
        if self.health < 0:
            self.health = 0

    def show_stats(self):
        print(f"ðŸ“Š {self.name} | Style: {self.style} | HP: {self.health} | EN: {self.energy} | STR: {self.strength} | DEF: {self.defense} | SPD: {self.speed}")

    def take_turn(self, opponent):
        if self.stunned:
            print(f"ðŸ˜µ {self.name} is stunned and skips the turn!")
            self.stunned = False
            return
        self.ability.execute(self, opponent)


